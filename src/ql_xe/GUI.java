    private void Exit_MenuChangeColor(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Exit_MenuChangeColor
        // TODO add your handling code here:
        JPanel temp = (JPanel) evt.getSource();
        temp.setBackground(new Color(244,244,244));
        if (temp.equals(Panel_Exit))
            temp.setBackground(new Color(255,255,255));
        if (temp.equals(Panel_ButtonMenu))
            temp.setBackground(new Color(0,0,0));
        if (temp.equals(pMenu)){
            temp.setBackground(new Color(255,255,255));
                    this.requestFocus();
        }
        
    }//GEN-LAST:event_Exit_MenuChangeColor

